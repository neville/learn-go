package main

import "fmt"

func main() {
	// Numeric constants
	const pi = 3.1417
	fmt.Println("Value of pi is", pi)

	const price float32 = 150.50
	fmt.Println("Value of price is", price)

	// String constants
	const name = "Neville"
	fmt.Println("Value of name is", name)

	const city string = "Mumbai"
	fmt.Println("Value of city is", city)

	// Boolean constants
	const success = true
	fmt.Println("Value of success is", success)

	const enabled bool = false
	fmt.Println("Value of enabled is", enabled)
}
